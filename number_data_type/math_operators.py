#Mathematical Operators in Python

print('Addition')
print(100 + 42)

print('Subtraction')
print(100 - 42)

print('Division')
print(100 / 42)
print(100 / 38)

print('Floor Division')
print(100 // 42)
print(100 // 38)

print('Multiplication')
print(100 * 42)

print('Modulus')
print(100 % 42)
print(2 % 2) #Returns 0
print(2 % 5) #Returns 1
print(25 % 2) #Returns 1
#Modulus is the remainder after dividing one number by another
#Divides into number as many times as possible and returns the remainder
#If it's a even number you get 0 and odd will return 1
#Great for checking if a number is even or odd.

print('Exponents')
print(100 ** 42)