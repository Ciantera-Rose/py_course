#Single quote strings
#Double quote strings
#Single and double quotes cannot be combined.
#Stick to one format. Either single or double throughout your document.

#A basic sentence with single quotes. Same if used with double quotes. Not both together.
sentence = 'The quick brown fox jumped over the lazy dog'

#You may use a backslash for instances like an apostrophe to let Python know to not read it as a string literal.
sentence_two = 'That is my dog\'s bowl'

#May use double quotes and a single quote as an apostrophe with Python throwing an error.
sentence_three = "That is my dog's bowl"

#May use backslashes before and after a quotaion to prevent Python throwing an error.
sentence_four = "Tiffany said, \"That is my dog's bowl\""